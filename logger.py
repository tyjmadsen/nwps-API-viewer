import logging

# Configure logging
logging.basicConfig(
    filename="app.log",
    level=logging.INFO,
    format="%(asctime)s - %(levelname)s - %(message)s"
)

logger = logging.getLogger("hydro_viewer")
logger.setLevel(logging.DEBUG)

# And if youâ€™re using a stream handler:
ch = logging.StreamHandler()
